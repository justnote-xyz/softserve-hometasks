/* Задание №11. Функции
Напишите функцию pow(x, n), которая возвращает x в степени n. Иначе говоря,
умножает x на себя n раз и возвращает результат. */

 function pow(x, n) {
    var res = x;
    
        for (var i = 1; i < n; i++) {
            res *= x
        }
        return res;
}
